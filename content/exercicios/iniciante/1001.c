#include <stdio.h>

int main() {
    
    // Declarando três variáveis de valores inteiros
    int a, b, x;
    // Lendo os valores das duas primeiras variáveis
    scanf("%d %d", &a, &b);

    // Afirmando que o valor da terceira variável é a soma das outras duas variáveis
    x = a + b;
 
    // Exibindo a mensagem requisitada com o valor da terceira variável "x"
    printf("X = %d\n", x);

    // Retornando 0 para indicar que o programa foi executado com sucesso
    return 0;
}
